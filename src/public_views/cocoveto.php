    <article id="find">
        <h2>Comment ai-je trouvé ce stage ?</h2>
        <p>
            Pour valider ma première année de BTS SIO, je devais réaliser un stage d'une durée de 6 semaines dans une entreprise en tant que développeur.
            <strong>Robin Hoffmeister</strong>, le fondateur de <strong>CocoVeto</strong>, a été démarché par une camarade de classe. Il lui a dit qu'elle devait trouver un camarade
            de classe pour réaliser un stage de développement chez lui. Elle m'a donc proposé de réaliser ce stage avec elle. Nous avons passé un entretien, et nous avons été reçus.
        </p>
    </article>

    <article id="activities">
        <h2>Qu'ai-je fait chez CocoVeto ?</h2>
        <section>
            <h3>Quelle problématique ?</h3>
            <p>La startup a pour but de fournir une assistance vétérinaire aux agriculteurs dans des zones reculées, dans des pays étrangers notamment, ainsi que de leur fournir des conseils.</p>
        </section>

        <section>
            <h3>Les objectifs :</h3>
            <ul>
                <li>Apporter des améliorations sur son application de télémédecine existante,</li>
                <li>Développer de nouveaux services d'aides aux agriculteurs.</li>
            </ul>
        </section>

        <section>
            <h3>Les technologies utilisées :</h3>
            <ul>
                <li>Flutter</li>
                <li>Web</li>
                <li>API</li>
            </ul>
        </section>

        <section>
            <h3>Les difficultés rencontrées :</h3>
            <ul>
                <li>La communication : la personne en charge du développement ne travaillait pas dans l'entreprise.</li>
                <li>La gestion du temps : le stage était court, et il fallait être efficace.</li>
                <li>Les demandes : Pas de cahier des charges, et des requêtes provenant du maître de stage journalières et décousues.</li>
            </ul>
        </section>

        <section>
            <h3>Les réussites :</h3>
            <ul>
                <li>Amélioration de l'application existante (temps de chargement, base de données, design...).</li>
                <li>Création de nouveaux services (API, Web, Flutter).</li>
            </ul>
        </section>

        <p>Nous avons interdiction de montrer le code de l'application, ou même un aperçu. Cependant, voici ce en quoi consiste l'application :</p>
        <ul>
            <li>Récupération, traitement et analyse des données du marché des denrées alimentaires,</li>
            <li>Implémentation d'un chatbot avec ChatGPT pour que l'agriculteur puisse poser des questions sur l'alimentation de son troupeau,</li>
            <li>Affichage de fiches conseil par rapport aux maladies et à l'alimentation,</li>
            <li>Appel vers un conseiller.</li>
        </ul>
    </article>

