    <h1>Mon expérience chez Lyreco</h1>
    <article id="find">
        <h2>Comment ai-je trouvé ce stage ?</h2>
        <p>
            Dans le cadre de mon emploi étudiant au sein de la société Menissez, j'ai été mis en contact avec un ancien chef de projet de chez Lyreco.
            Je lui ai dit que je recherchais un stage, et il m'a proposé de me mettre en lien avec Baptiste Dhaussy, le chef de projet actuel, afin de faire un entretien pour éventuellement obtenir un stage.
        </p>
    </article>

    <article id="activities">
        <h2>Qu'ai-je fait chez Lyreco ?</h2>
        <section>
            <h3>Quelle problématique ?</h3>
            <p>
                J'ai travaillé dans le service informatique de Lyreco France, et j'avais pour objectif de faire du développement interne.
                Les commerciaux de Lyreco utilisaient un fichier Excel, nommé CleanPack, pour créer des contrats, gérer des consommables et des distributeurs, ...
                Ce fichier Excel était devenu obsolète, et il fallait le remplacer par une application web.
            </p>
        </section>

        <section>
            <h3>Les objectifs :</h3>
            <ul>
                <li>Analyser le fichier excel et en extraire les formules,</li>
                <li>Comprendre le fonctionnement du JWT,</li>
                <li>Comprendre le fonctionnement du SSO de Microsoft,</li>
                <li>Créer une base de donnée (SQLite),</li>
                <li>Créer le backend (Génération de contrat, génération de cadencement) en PHP,</li>
                <li>Créer le frontend à l'aide de TailWind et TypeScript,</li>
            </ul>
        </section>

        <section>
            <h3>Les technologies utilisées :</h3>
            <ul>
                <li>PHP</li>
                <li>TypeScript</li>
                <li>TailWind</li>
            </ul>
        </section>

        <section>
            <h3>Les difficultés rencontrées :</h3>
            <ul>
                <li>Les formules du fichier excel étaient complexes et le code n'était pas commenté.</li>
                <li>Le JWT et le SSO de Microsoft étaient des technologies que je ne connaissais pas.</li>
                <li>La base de donnée devait être en SQLite, et je n'avais jamais utilisé cette technologie.</li>
                <li>Le frontend devait être fait en TailWind et TypeScript, et je n'avais jamais utilisé ces technologies.</li>
                <li>A de nombreuses reprises, j'ai dû reprendre le code que j'avais fait du PHP vers Js, puis vers TypeScript et ainsi de suite.</li>
            </ul>
        </section>

        <section>
            <h3>Les réussites :</h3>
            <ul>
                <li>J'ai réussi à comprendre les formules du fichier excel, et à les retranscrire en PHP.</li>
                <li>J'ai réussi à comprendre le fonctionnement du JWT et du SSO de Microsoft.</li>
                <li>J'ai réussi à créer une base de données en SQLite.</li>
                <li>J'ai réussi à créer le backend en PHP.</li>
                <li>J'ai réussi à créer le frontend en TailWind et TypeScript.</li>
                <li>Le projet était fini à temps.</li>
            </ul>

