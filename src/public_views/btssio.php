    <h1>BTS SIO</h1>
    <article id="what-is-it">
        <h2>Qu'est-ce que c'est ?</h2>
        <p>Le BTS SIO est une formation en informatique de deux ans qui forme des professionnels capables de répondre aux besoins informatiques des organisations.</p>
        <p>Les étudiants y acquièrent des compétences en développement, en gestion de projet, en maintenance et en support informatique.</p>
        <p>Le BTS SIO propose deux spécialités :</p>
        <ol>
            <li>
                <h3>SISR - Solutions d'Infrastructure, Systèmes et Réseaux</h3>
                <p>Cette spécialité se concentre sur la gestion des infrastructures informatiques, la configuration des systèmes et la gestion des réseaux.</p>
            </li>
            <li>
                <h3>SLAM - Solutions Logicielles et Applications Métiers</h3>
                <p>La spécialité SLAM se focalise sur le développement d'applications métiers, la programmation, et la création de solutions logicielles pour les entreprises.</p>
            </li>
        </ol>
    </article>

    <article id="why-slam">
        <h2>Mon choix de spécialité</h2>
        <p>Après le bac, je suis parti en licence SESI pour faire de la physique chimie. Cependant, j'ai toujours été attiré par l'informatique, mais on n'a eu de cesse de me dire que ce n'était pas pour moi.</p>
        <p>Lors de ma première année de licence, j'ai eu un cours de programmation en Python, et j'ai adoré ça. J'ai donc décidé de me réorienter et de faire de la programmation, car c'était devenu une passion et je ne faisais plus que ça.</p>
        <p>C'est pourquoi j'ai décidé de faire un BTS SIO, et de choisir la spécialité SLAM.</p>
    </article>

